#!/bin/bash

clear

# 获取终端宽度
cols=$(tput cols)

# 定义佛祖 ASCII 艺术图案
read -r -d '' art << 'EOF'
.................阿弥陀佛..................
 _oo0oo_
o8888888o
88“.”88
(| -_- |)
0\ = /0
.' \| |/ '.
//\\\||| : |||///\\
//_||||| -卍- ||||_\\
| | \\\\\\ - ////// | |
| \_| ''\\---/'' |_/ |
\ .-\\__ '-' __//-. /
__'. .' /--.--\ '. .'__
.“” '< '.__\\_<|>_//__.' >' “”.
| | : '- \\ '.: '\\ _ //':.' // - ' : | |
\ \ '_. \_ __ \ /__ _/ .-' / /
==== '-.___ '.__ \_____/ __.' ___.-' ====
'=---='
...............佛祖保佑.没有bug.....................
EOF

# 逐行读取并居中打印
while IFS= read -r line; do
    # 计算每行的字符长度（考虑多字节字符）
    len=$(echo -n "$line" | wc -m)

    # 计算前导空格数
    padding=$(( (cols - len) / 2 ))

    # 如果padding为负数，设置为0
    if [ "$padding" -lt 0 ]; then
        padding=0
    fi

    # 打印前导空格和内容
    printf "%*s%s\n" "$padding" "" "$line"
done <<< "$art"

